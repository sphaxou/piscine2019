#include "ft.h"

void	ft_putnbr(int nb)
{
	long	n;

	n = nb;
	if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n / 10);
		ft_putchar(-n % 10 + '0');
	}
	else
	{
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}

}
