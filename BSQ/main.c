#include "ft.h"

int main(int ac, char **av)
{
	int i;

	if (ac == 1)
		return (readinput());
	i = 1;
	while (i < ac)
	{
		bsq(av[i++]);
		if (i != ac)
			ft_putstr("\n");
	}
	return (0);
}
