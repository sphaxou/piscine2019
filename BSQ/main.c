#include "ft.h"

int main(int ac, char **av)
{
	int i;

	if (ac == 1)
	{
		ft_putstr_fd("No argument\n", 2);
		return (0);
	}
	i = 1;
	while (i < ac)
	{
		bsq(av[i++]);
		if (i != ac)
			ft_putstr("\n");
	}
	return (0);
}
